package encapsulation;

public class Encapsulation {
	private int a;
	private float b;
	private double c;
	private String d;
	public void setA(int a) {
		this.a=a;
	}
	public int getA() {
	  return a;
	}
	public void setB(float b) {
		this.b=b;
	}
	public float getB() {
	  return b;
	}
	public void setC(double c) {
		this.c=c;
	}
	public double getC() {
	  return c;
	}
	public void setD(String d) {
		this.d=d;
	}
	public String getD() {
	  return d;
	}
	
	
	

}
